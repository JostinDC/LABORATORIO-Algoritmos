# Mayúsculas y minúsculas: Escribe un programa que convierta una cadena dada por el usuario a 
# mayúsculas y luego a minúsculas, imprimiendo cada resultado.

cadena = input("Introduzca una cadena: ")

cadena_mayus = cadena.upper()
print("La cadena en mayúscula vendria a ser:",cadena_mayus)

cadena_minus = cadena.lower()
print("La cadena en minúscula vendria a ser:",cadena_minus)